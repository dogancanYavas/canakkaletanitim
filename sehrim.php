                <h1>Çanakkale</h1>
                <p>
                Çanakkale antik çağlara kadar uzanan geçmişi ve binlerce yıllık zenginliği ile Türkiye’nin en önemli şehirlerinden biridir. Türkiye Cumhuriyeti’nin kuruluş dönemlerinde hayati bir rol oynayan Çanakkale’nin Marmara ve Ege bölgelerinde toprakları bulunur.
                </p>
                <h2>Gezilebilecek Yerler</h2>
                <p>
                Truva Antik Kenti ve Assos Antik Kenti başta olmak üzere dünya için önemli antik kentlere de ev sahipliği yapan Çanakkale, yaz tatillerinin favorisi olan ilçe ve adaları da bünyesinde barındırır.                 </p>
                <p>
                Gökçeada, Bozcaada ve Ayvacık gibi özellikle yaz aylarında hareketlenen birçok yer Çanakkale’de bulunur.                </p>
                <p>
                Aynı zamanda birbirinden güzel Çanakkale otelleri, turistleri kendine çeker. Sizler için Çanakkale’de mutlaka görülmesi gereken yerleri şu şekilde sıralayabiliriz:                </p>
                <p>
                	Truva Antik Kenti  , Çanakkale Şehitleri Anıtı ve Harp Müzesi , Aynalı Çarşı                </p>
                <p>
                Dört mevsim boyunca ziyaret edilen Çanakkale, yazları sıcak, kışları ise yağışlıdır.                 </p>
                <p>
                Hem Marmara hem de Ege ikliminin görüldüğü şehirde tatil için yapılan ziyaretler genellikle mayıs ve eylül aylarının arasında gerçekleşir.                </p>
                <p>
                Şehre gelmek için Çanakkale Havalimanı kullanılabilir. Deniz ve kara yolu ile de ulaşım sağlanabilir.                </p>

